     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $faktur = new Faktur();
        $faktur->faktur_code = $request->faktur_code;
        $faktur->created_by = $request->created_by;
        $faktur->created_at = now();
        $faktur->updated_at = now();
        $faktur->save();
        return redirect()->route('admin.faktur.index')->with('success', 'e-Faktur Added');
    }

